#include "libftprintf.h"

void	ft_printd(int n)
{
	if (n == -2147483648)
		ft_prints("-2147483648");
	else if (n < 0)
	{
		ft_printc('-');
		ft_printd(-n);
	}
	else
	{
		if (n > 9)
			ft_printd(n / 10);
		ft_printc(n % 10 + '0');
	}
}
