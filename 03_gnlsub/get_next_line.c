#include "get_next_line.h"

char	*get_next_line(int fd)
{
	static char	*buffer[1024];
	char		*theline;
	int			bytesread;

	buffer[fd] = ft_calloc(BUFFER_SIZE + 1, sizeof(char));
	if (buffer[fd] == NULL)
		return (NULL);
	bytesread = read(fd, buffer[fd], BUFFER_SIZE);
	if (bytesread < 1)
		return (NULL);
	theline = buffer[fd];
	return (theline);
}
